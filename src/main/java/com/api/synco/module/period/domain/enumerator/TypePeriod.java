package com.api.synco.module.period.domain.enumerator;

/**
 * Enumeration representing types of class periods.
 *
 * <p>This enum defines the different time slots when classes
 * can be scheduled throughout the day.</p>
 *
 * @author Luca5Eckert
 * @version 1.0.0
 * @since 1.0.0
 */
public enum TypePeriod {
    /**
     * Morning period (typically 7:00 AM - 12:00 PM).
     */
    MORNING,
    
    /**
     * Afternoon period (typically 12:00 PM - 6:00 PM).
     */
    AFTERNOON,
    
    /**
     * Evening period (typically 6:00 PM - 10:00 PM).
     */
    EVENING

}
